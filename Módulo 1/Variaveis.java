//Variáveis
public class Variaveis {
    
    public static void main(String[] args){
        //Declaração de variável
        int valor;
        valor = 1;
        System.out.println(valor);

        //Declarando e inicializando algumas variáveis
        int num4, num5 = 4, num6 = 7, num8;

        num4 = 2;
        num8 = 5;
        System.out.println(num4 + num5 + num6 + num8);

        char caractere = 'a';
        //por padrão, os valores de ponto flutuante são double no Java
        // o "f" serve para dizer ao Java que é floa
        float preco = 2.0f;

        System.out.println(caractere + preco); 

        double _valor2 = 34.2;
        System.out.println(_valor2);

    }

}
